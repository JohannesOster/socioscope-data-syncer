#!/usr/bin/env node
/**
 * Convert a Lumi-exported layers CSV into a data-syncer structured-spans JSON.
 *
 * Lumi's layers CSV columns: case_id, turn, speaker, quote, label, notes,
 * layer_name, coder, source, score
 *
 * Lumi spans are anchored to (turn-index, quote-text), not character offsets.
 * This script finds each quote in the transcript text and computes the
 * start/end offsets that the data-syncer schema requires.
 *
 * Usage:
 *   node tools/lumi-to-spans.mjs \
 *     --layers /path/to/lumi-layers.csv \
 *     --transcript /path/to/INTERVIEW_VIDEO-TRANSCRIPTION-...csv \
 *     --case-id AM-002 \
 *     --source-file AM-002-...-INTERVIEW_VIDEO-TRANSCRIPTION-...csv \
 *     --out /path/to/output.json
 *
 * If --transcript is given multiple times, the script picks the file where the
 * most quotes match.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, basename, dirname, join } from "node:path";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const Papa = require(
  resolve(dirname(import.meta.url.replace("file://", "")), "..", "node_modules", "papaparse", "papaparse.js"),
);

function parseArgs(argv) {
  const out = { transcripts: [] };
  for (let i = 0; i < argv.length; i++) {
    const k = argv[i];
    const v = argv[i + 1];
    switch (k) {
      case "--layers": out.layers = v; i++; break;
      case "--transcript": out.transcripts.push(v); i++; break;
      case "--case-id": out.caseId = v; i++; break;
      case "--source-file": out.sourceFile = v; i++; break;
      case "--out": out.out = v; i++; break;
      case "-h":
      case "--help":
        out.help = true; break;
    }
  }
  return out;
}

const args = parseArgs(process.argv.slice(2));
if (args.help || !args.layers || args.transcripts.length === 0 || !args.caseId || !args.out) {
  console.error(
    "Usage: lumi-to-spans.mjs --layers FILE --transcript FILE [--transcript FILE...] --case-id CASE --out FILE [--source-file NAME]",
  );
  process.exit(args.help ? 0 : 2);
}

function pickField(row, candidates) {
  for (const c of candidates) {
    const v = row[c] ?? row[c.toUpperCase()];
    if (v && String(v).trim()) return String(v);
  }
  return "";
}

function parseTurns(csv) {
  const r = Papa.parse(csv, { header: true, skipEmptyLines: true });
  return (r.data ?? [])
    .map((row) => ({
      speaker: pickField(row, ["SPEAKER", "speaker"]).trim(),
      text: pickField(row, [
        "USER REVISION",
        "TRANSLATION",
        "TRANSCRIPTION",
      ]).trim(),
    }))
    .filter((t) => t.speaker || t.text);
}

function findInTurns(turns, quote, hintIdx, hintSpeaker) {
  // Try (a) the hinted index, (b) ±1 (Lumi sometimes 1-indexes), (c) full scan.
  const seen = new Set();
  const candidates = [];
  function push(i) {
    if (!seen.has(i)) { seen.add(i); candidates.push(i); }
  }
  if (Number.isFinite(hintIdx)) { push(hintIdx); push(hintIdx - 1); push(hintIdx + 1); }
  for (let i = 0; i < turns.length; i++) push(i);

  for (const idx of candidates) {
    const turn = turns[idx];
    if (!turn) continue;
    if (
      hintSpeaker &&
      turn.speaker &&
      turn.speaker.toLowerCase() !== hintSpeaker.toLowerCase()
    ) continue;
    const pos = turn.text.indexOf(quote);
    if (pos >= 0) return { turn: idx, start: pos };
  }
  return null;
}

const layerRows = Papa.parse(readFileSync(args.layers, "utf8"), {
  header: true,
  skipEmptyLines: true,
}).data;
console.log(`Layer rows: ${layerRows.length}`);

let best = null;
for (const tpath of args.transcripts) {
  const turns = parseTurns(readFileSync(tpath, "utf8"));
  let matched = 0;
  const spans = [];
  for (const r of layerRows) {
    const hintIdx = parseInt(r.turn, 10);
    const hit = findInTurns(turns, r.quote, hintIdx, r.speaker);
    if (!hit) continue;
    matched++;
    spans.push({
      turn: hit.turn,
      start: hit.start,
      end: hit.start + r.quote.length,
      quote: r.quote,
      label: r.label || undefined,
      notes: r.notes || undefined,
    });
  }
  console.log(`  ${basename(tpath)}: matched ${matched}/${layerRows.length} (${turns.length} turns)`);
  if (!best || matched > best.matched) {
    best = { path: tpath, matched, spans };
  }
}

if (!best || best.matched === 0) {
  console.error("No quotes matched any transcript. Aborting.");
  process.exit(1);
}

// File-level metadata from the first row that has it.
const sample = layerRows.find((r) => r.layer_name) ?? layerRows[0] ?? {};
const layer = sample.layer_name?.trim();
const coder = sample.coder?.trim();
const source = sample.source?.trim();
const description = coder || source
  ? `Generated by ${coder ?? "unknown"}${source ? ` (${source})` : ""}.`
  : undefined;

const sourceFile = args.sourceFile ?? basename(best.path);

const output = {
  caseId: args.caseId,
  sourceFile,
  ...(layer ? { layer } : {}),
  ...(description ? { description } : {}),
  spans: best.spans,
};

writeFileSync(args.out, JSON.stringify(output, null, 2));
console.log(`\n✓ Wrote ${best.spans.length} spans → ${args.out}`);
console.log(`  caseId:     ${args.caseId}`);
console.log(`  sourceFile: ${sourceFile}`);
if (layer) console.log(`  layer:      ${layer}`);
